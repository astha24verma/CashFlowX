    public Balance(String ac, String name) {
        initComponents();
        ac_no = ac;
        userName = name;
        try {
            DBConnection db = new DBConnection();
            db.pstmt = db.con.prepareStatement("select amount from open_account_table where ac_no=?");
            db.pstmt.setString(1, ac_no);
            db.rst = db.pstmt.executeQuery();
            if (db.rst.next()) {
                String amt = db.rst.getString(1);
                BalanceTextField.setText("â‚¹ "+amt);
                L4.setText(userName);
                BalanceTextField.setEditable(false);
            } else {
                JOptionPane.showMessageDialog(this, "Inccorect Account Number.");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

