    private void jPasswordField1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jPasswordField1KeyPressed
        //Validation for PIN
        String str = jPasswordField1.getText();
        for (int i = 0; i < str.length(); i++) {
            char ch = str.charAt(i);
            int asc = ch;
            if (!(asc >= 48 && asc <= 57)) {
                JOptionPane.showMessageDialog(this, "Invalid PIN, Enter digits only");
                jPasswordField1.setText("");
            }
        }
        String str2 = jPasswordField1.getText();
        if (jPasswordField1.getText().length() > 3) {
            JOptionPane.showMessageDialog(this, "PIN cannot be more than 4 Digits");
            jPasswordField1.setText(str2.substring(0, 4));
        }
    }//GEN-LAST:event_jPasswordField1KeyPressed

