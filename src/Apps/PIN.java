    private void jLabel19MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel19MouseClicked
        // Code on Confirm label 19 in PIN authentication of amount WITHDRAWAL
        try {
            String pin = jPasswordField1.getText();
            DBConnection db = new DBConnection();
            // Q1 -- To CHECK PIN and account number
            db.pstmt = db.con.prepareStatement("select amount from open_account_table where password=? and ac_no=?");
            db.pstmt.setString(1, pin);
            db.pstmt.setString(2, acNum);

            db.rst = db.pstmt.executeQuery();

            if (db.rst.next()) {
                int totalAmt = Integer.parseInt(db.rst.getString(1));
                if (cashAmt <= totalAmt) {
                    totalAmt = totalAmt - cashAmt;
                    // Q2 --  Update total amt 
                    db.pstmt = db.con.prepareStatement("update open_account_table set amount=? where ac_no=? ");
                    db.pstmt.setInt(1, totalAmt);
                    db.pstmt.setString(2, acNum);
                    int i = db.pstmt.executeUpdate();
                    if (i > 0) {
                        //Q3 -- Add traction amt to table (transaction)
                        db.pstmt = db.con.prepareStatement("insert into transaction values(?,?,?,?,?)");
                        db.pstmt.setString(1, acNum);
                        db.pstmt.setString(2, new java.util.Date().toString());
                        db.pstmt.setInt(3, cashAmt);
                        db.pstmt.setString(4, type);
                        db.pstmt.setInt(5, totalAmt);
                        int j = db.pstmt.executeUpdate();
                        if (j > 0) {

                            new Success(acNum, cashAmt, totalAmt, type).setVisible(true);
                            this.setVisible(false);
                        } else {
                            JOptionPane.showMessageDialog(this, "Error in Addition of details in transaction table !");
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "Error in Amount Updation !");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, " Insufficient Balance in your Account!");
                    new Withdraw_Amount(acNum).setVisible(true);
                    this.setVisible(false);
                }

            } else {
                JOptionPane.showMessageDialog(this, " Incorrect PIN !");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jLabel19MouseClicked

